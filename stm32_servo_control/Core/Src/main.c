static void send_echo(const char* message) {
    char tx_buffer[256];
    sprintf(tx_buffer, "Echo from STM32: %s\n", message);
    int length = strcspn(tx_buffer, "\n");
    HAL_Delay(500);
    HAL_UART_Transmit(&huart4, (uint8_t*)tx_buffer, length, HAL_MAX_DELAY);
}

